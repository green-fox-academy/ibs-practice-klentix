#create a list
finance = [500, 1000, 1250, 175, 800, 120]

# sum all elements in a list
print("How much did we spend? ", sum(finance))

#Which was our greatest expense?
print ("Which was our greatest expense?", max(finance))

#Which was our cheapest spending?
print("Which was our cheapest spending?", min(finance))

#What was the average amount of our spendings?
print("What was the average amount of our spendings?", sum(finance)/len(finance))